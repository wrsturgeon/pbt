/// An iterator that starts inactive,
/// remains active until it has been exhausted,
/// and then may be started again.
#[derive(Debug, Default)]
pub enum MaybeIterator<T: Exhaust> {
    /// If and only if there is an active iterator that has not returned `None`.
    Active {
        /// The internal exhaustive iterator over `T`.
        iter: T::Exhaust,
    },
    /// Either not yet started or just finished.
    #[default]
    Inactive,
}

/// An iterator that starts inactive,
/// remains active until it has been exhausted
/// (caching the most recent item until asked to advance),
/// and then may be started again.
#[derive(Debug, Default)]
pub enum CachingIterator<T: Clone + Exhaust> {
    /// If and only if there is an active iterator that has not returned `None`.
    Active {
        /// The value-size of `T`s generated by `iter`.
        size: usize,
        /// The internal exhaustive iterator over `T`.
        iter: T::Exhaust,
        /// The most recent item, if any.
        cache: Option<T>,
    },
    /// Either not yet started or just finished.
    #[default]
    Inactive,
}

/// Inductively create a nested tuple,
/// then flatten each generated value.
#[derive(Debug)]
pub struct FlattenNestedIterator<N: NestedIterator> {
    /// An iterator producing nested tuples of any given size.
    nested_iterator: N,
    /// The total value-size of each tuple as a whole.
    total_size: usize,
}

/// Produce a nested tuple of any given size.
pub trait NestedIterator {
    /// The nested tuple type to be produced.
    type Item;
    /// Produce the next nested tuple of a given size.
    /// TODO: move `remaining_size` to `self` instead! It shouldn't change until exhausted!
    fn nested_next(&mut self, remaining_size: usize) -> Option<Self::Item>;
    /// Assert that all downstream iterators are inactive (e.g. from having been restarted).
    #[cfg(debug_assertions)]
    fn debug_assert_all_inactive(&self);
}

impl<T: Exhaust> MaybeIterator<T> {
    /// Get the next item in an existing iterator if we're already using one.
    /// If there was an iterator, but it's now empty, drop it and return `None`.
    /// If there was no iterator in here in the first place, try to start one;
    /// then, if and only if that succeeds *and* it is non-empty, return its first item.
    /// # Invariant
    /// If this function returns `Some`, then
    /// this `enum` is `MaybeIterator::Active { .. }` afteward;
    /// if not, this is `MaybeIterator::Inactive` afteward
    /// (either from clearing after an iterator ended or from failing to start a new one).
    #[inline]
    pub(crate) fn next_or_new(&mut self, remaining_size: usize) -> Option<T> {
        match *self {
            Self::Active { ref mut iter } => iter.next().or_else(|| {
                *self = Self::Inactive;
                None
            }),
            Self::Inactive => {
                // Unlike non-final fields, the field at the end of the tuple
                // has to be *exactly* `remaining_size` to work.
                if let Ok(mut iter) = T::exhaust(remaining_size)
                    && let Some(next) = iter.next()
                {
                    *self = Self::Active { iter };
                    Some(next)
                } else {
                    None
                }
            }
        }
    }
}

impl<T: Exhaust> NestedIterator for MaybeIterator<T> {
    type Item = T;
    #[inline]
    fn nested_next(&mut self, remaining_size: usize) -> Option<Self::Item> {
        self.next_or_new(remaining_size)
    }

    #[inline]
    #[cfg(debug_assertions)]
    fn debug_assert_all_inactive(&self) {
        match *self {
            Self::Inactive => {}
            Self::Active { .. } => {
                panic!(
                    "Expected all downstream iterators to be inactive, but the final `MaybeIterator` was active"
                )
            }
        }
    }
}

impl<T: Clone + Exhaust> CachingIterator<T> {
    /// Regardless of whether there's currently any value cached,
    /// advance this iterator (if possible) and update the cache accordingly.
    /// If the iterator could not be advanced, use the provided argument
    /// to restart at another size (if any, otherwise immediately failing),
    /// then loop downward from that provided value (if any)
    /// until a non-empty iterator could be created,
    /// failing if that loop ends before succeeding.
    #[inline]
    pub fn fill_cache_with_next_value(
        &mut self,
        maybe_remaining_size: Option<usize>,
    ) -> Option<()> {
        if let Self::Active {
            ref mut iter,
            ref mut cache,
            ..
        } = *self
        {
            if let Some(next) = iter.next() {
                *cache = Some(next);
                return Some(());
            }
            *self = Self::Inactive;
        }
        self.new_iterator_with_size_at_most(maybe_remaining_size?)
    }

    /// Check if this iterator has a value already cached,
    /// without actually returning the value (to avoid lifetime extension).
    #[inline]
    pub const fn has_value_cached(&self) -> bool {
        matches!(
            self,
            Self::Active {
                cache: Some(..),
                ..
            },
        )
    }

    /// Start a new iterator at the provided size;
    /// if it produces a value, cache it and return;
    /// if not, iteratively reduce size and create new iterators until one does,
    /// or return `None` if we exhaust all sizes from the argument down to zero.
    #[inline]
    fn new_iterator_with_size_at_most(&mut self, remaining_size: usize) -> Option<()> {
        for size in (0..=remaining_size).rev() {
            // TODO: Binary search for reachability, then linear descent?
            let Ok(mut iter) = T::exhaust(size) else {
                continue;
            };
            let Some(cached) = iter.next() else { continue };
            *self = Self::Active {
                size,
                iter,
                cache: Some(cached),
            };
            return Some(());
        }
        *self = Self::Inactive;
        None
    }
}

impl<T: Clone + Exhaust, Tail: NestedIterator> NestedIterator for (CachingIterator<T>, Tail) {
    type Item = (T, Tail::Item);
    #[inline]
    fn nested_next(&mut self, remaining_size: usize) -> Option<Self::Item> {
        // Syntactic operation only: split `self` into `head` and `tail`:
        let (ref mut head_iter, ref mut tail_iter) = *self;

        // Each element of the tuple (left to right) should keep the same cached value
        // until the elements to the right of it have been fully exhausted,
        // at which point (and ONLY at which point) it should advance with all downstream restarted.
        // TODO: In practice, this particular `if` condition is only ever satisfied
        // when starting the overall iterator; maybe consider removing the `Inactive` variant
        // and moving the startup logic into the initialization (which then could fail)
        // so we could remove this and improve efficiency in this very hot loop?
        if !head_iter.has_value_cached() {
            #[cfg(debug_assertions)]
            {
                // Another logical invariant arises from the logic around these iterators
                // and the structure of the below loop (which in turn relies on this invariant):
                // **anytime one iterator is `Inactive`, so are all those after it.**
                let () = tail_iter.debug_assert_all_inactive();
            }
            // Fill the cached value to establish the loop invariant below,
            // or exit with `None` if the iterator is empty:
            let () = head_iter.fill_cache_with_next_value(Some(remaining_size))?;
        }
        // Duh, but worth checking anyway (at least until v1.0):
        debug_assert!(head_iter.has_value_cached());

        // LOOP INVARIANT (established above):
        // `head_iter` is `Active` with a value in its `cache` (as `Some(..)`).
        // If this fails at any point, this function returns `None`.
        loop {
            // Check the loop invariant (in debug builds only):
            debug_assert!(head_iter.has_value_cached());

            // Get the cached head value, which we know exists b/c of the loop invariant above:
            let CachingIterator::Active {
                size: head_size,
                cache: Some(ref head_cached),
                ..
            } = *head_iter
            else {
                // SAFETY:
                // Just established above (before the loop),
                // and the end of the loop re-establishes the invariant as well.
                unsafe { unreachable_unchecked() }
            };

            // Subtract the head size from the remaining size (for the tail):
            // Note that this isn't using `checked_sub`, since _a priori_
            // the head can never be larger than `remaining_size`.
            // However, if this invariant were to be violated,
            // tests would pick it up, since Rust's `-` panics on overflow in debug builds.
            #[expect(clippy::arithmetic_side_effects, reason = "Intentional: see above.")]
            let tail_size = remaining_size - head_size;

            if let Some(tail) = tail_iter.nested_next(tail_size) {
                return Some((head_cached.clone(), tail));
            }

            // Check that all downstream iterators are inactive and ready to be restarted:
            #[cfg(debug_assertions)]
            let () = tail_iter.debug_assert_all_inactive();

            // Then update the cache with the next value for this index of the tuple,
            // implicitly restarting all downstream iterators (checked above):
            let () = head_iter.fill_cache_with_next_value(head_size.checked_sub(1))?;
        }
    }

    #[inline]
    #[cfg(debug_assertions)]
    fn debug_assert_all_inactive(&self) {
        let (ref head, ref tail) = *self;
        match *head {
            CachingIterator::Active {
                size, ref cache, ..
            } => panic!(
                "Expected all downstream iterators to be inactive, but a caching iterator was active with size {size:?} and cache {cache:?}",
            ),
            CachingIterator::Inactive => tail.debug_assert_all_inactive(),
        }
    }
}

