/// Let's say we're iterating over fields in a tuple,
/// and we know the maximum size of each field (unless undecidable, overflowed, etc.).
/// Let's further suppose that we have an overall goal size in mind for the whole tuple.
/// Then if we split the size evenly over all fields, it would be applied unevenly in general:
/// some fields are uninstantiable, and some have a finite size lower than the even split.
/// This `struct` holds the result of counting those quantities.
struct FullAndPartialFields {
    /// The number of fields that could generate at least the requested expected AST size.
    n_full_fields: usize,
    /// The sum of the sizes of each field that could not generate the requested AST size.
    sum_of_partial_sizes: f32,
}

/// Let's say we're iterating over fields in a tuple,
/// and we know the maximum size of each field (unless undecidable, overflowed, etc.).
/// Let's further suppose that we have an overall goal size in mind for the whole tuple.
/// Then if we split the size evenly over all fields, it would be applied unevenly in general:
/// some fields are uninstantiable, and some have a finite size lower than the even split.
/// This function counts those quantities.
#[inline]
const fn full_and_partial_fields(
    even_split: f32,
    max_expected_ast_sizes: &[MaybeDecidable<Max<f32>>],
) -> FullAndPartialFields {
    match *max_expected_ast_sizes {
        [] => FullAndPartialFields {
            n_full_fields: 0,
            sum_of_partial_sizes: 0.,
        },
        [ref head, ref tail @ ..] => {
            let mut rec = full_and_partial_fields(even_split, tail);
            match *head.at_most() {
                Max::Uninstantiable => {}
                Max::Finite(max) if max < even_split => rec.sum_of_partial_sizes += max,
                #[expect(
                    clippy::arithmetic_side_effects,
                    reason = "If we overflow a `usize` by incremental addition, something has gone horribly wrong."
                )]
                Max::Finite(_) | Max::Infinite => rec.n_full_fields += 1,
            }
            rec
        }
    }
}

